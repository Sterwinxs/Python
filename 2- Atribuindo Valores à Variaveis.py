        # Atribuindo Valores à Variaveis. 
# Quando atribuimos um valor a uma Variavel é como se colocassemos um objeto em uma caixa e escrevessemos o que há nela por fora para utilizarmos, entretanto para atribuir um nome há algumas regras como:

# Devemos usar apenas letras, números ou sublinhados (não podemos usar apóstrofos, hífens, espaços, etc.).

# Os nomes de variáveis ​​não podem começar com um número.

# Utilizamos a variável também para podermos atualizar esse valor posteriormente em um código, então se resultado é igual a 2 posso mudar ele para 4 no meio do código por exemplo.

resultado = 10 * 2 #resultado é igual a 10 vezes 2.
print (resultado)

# Atualizando Resultado, sempre usamos ( +=, -=, *=, /=, **=) para atualizar uma variável. 

resultado += 30

print(resultado)